public class ContadorProgressivoFor {// declara a classe
    public static void main(String args[]) {//metodo principal que rodara a classe
        for (int i = 0; i < 10; i++) {//estrutura de repetição  que ira de 0 a 9
            System.out.println(i + " ");//a cada loop da for ele mostrara o valor de i
            //que esta sendo incrementado em 1 pelo proprio for
        }
        System.out.println("\nAcabou!");//informa que o loop se encerrou apos o loop se encerrar
    }
}